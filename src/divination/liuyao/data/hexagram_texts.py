def get_hexagram_text(name: str) -> HexagramText | None:
    """获取卦辞爻辞"""
    return HEXAGRAM_TEXTS.get(name)


def get_yao_text(hexagram_name: str, position: int) -> YaoText | None:
    """获取特定爻的爻辞"""
    hex_data = HEXAGRAM_TEXTS.get(hexagram_name)
    if not hex_data:
        return None
    for yao in hex_data['yao']:
        if yao['position'] == position:
            return yao
    return None


def get_high_emphasis_yaos(hexagram_name: str) -> list[int]:
    """获取高权重爻位"""
    hex_data = HEXAGRAM_TEXTS.get(hexagram_name)
    if not hex_data:
        return []
    return [yao['position'] for yao in hex_data['yao'] if yao['emphasis'] == 'high']


def has_timing_hint(hexagram_name: str, position: int) -> str | None:
    """检查是否有时间提示"""
    yao = get_yao_text(hexagram_name, position)
    return yao.get('timing') if yao else None


# 别名函数（兼容导入）
def get_hexagram_texts(name: str) -> HexagramText | None:
    """获取卦辞爻辞（别名）"""
    return get_hexagram_text(name)


def get_yao_texts(hexagram_name: str, position: int) -> YaoText | None:
    """获取特定爻的爻辞（别名）"""
    return get_yao_text(hexagram_name, position)
